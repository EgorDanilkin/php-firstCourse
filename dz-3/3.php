<?php

$regions = [
    'Московская область' => ['Москва', 'Красногорск', 'Клин'],
    'Якутия' => ['Якутск', 'Нюрба', 'Алдан'],
    'Алтайский край' => ['Барнаул', 'Бийск', 'Камень-на-оби']
];

while ($region = key($regions)) {
    echo $region . ':<br>';
    echo implode(', ', $regions[$region]) . '<br>';
    next($regions);
}